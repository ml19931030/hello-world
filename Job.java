package timeSum;

public class Job {
	private String jobId;
	private String jobTimeS;
	private String jobTimeE;
	public String getJobId() {
		return jobId;
	}
	public void setJobId(String jobId) {
		this.jobId = jobId;
	}
	public String getJobTimeS() {
		return jobTimeS;
	}
	public void setJobTimeS(String jobTimeS) {
		this.jobTimeS = jobTimeS;
	}
	public String getJobTimeE() {
		return jobTimeE;
	}
	public void setJobTimeE(String jobTimeE) {
		this.jobTimeE = jobTimeE;
	}




}
